# Exemplo de tupla simples
tupla_simples = (1, 2, 3, "a", "b", "c")
print("Tupla simples:", tupla_simples)

# Exemplo de tupla alinhada
tupla_aninhada = (1, (2, 3), 4)
print("Tupla aninhada:", tupla_aninhada)

# Desempacotamento de tupla aninhada
a, (b, c), d = tupla_aninhada
print("Desempacotamento da tupla aninhada:")
print("Desempacotamento a")
print("Desempacotamento b")
print("Desempacotamento c")
print("Desempacotamento d")